# lamda function--->anonymous(nameless) function

def add(num1,num2):
    return  num1+num2
print(add(10,20))

Add=lambda num1,num2:num1+num2
print(Add(10,20))
Sub=lambda  num1,num2:num1-num2
print(Sub(10,20))
Mul=lambda num1,num2:num1*num2
print(Mul(10,20))
Div=lambda num1,num2:num1/num2
print(Div(20,10))